using System;

namespace ConsoleApp1
{
    class Program
    {
        static void Main(string[] args)
        {
            /*0~10打印*/
            for (int i = 0; i <= 10; i++)
            {
                Console.WriteLine(i);
            }
            for (int i = 10; i > 0; i--)
            { Console.Write(i); }
            // Console.ReadLine();
        }
    }
    }
}
